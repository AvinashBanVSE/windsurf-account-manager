//! Devin Session authentication service
//!
//! Implements new authentication system based on Devin Session, including:
//! - `POST /_devin-auth/connections` Б─■ query available login methods for email
//! - `POST /_devin-auth/password/login` Б─■ account password login, returns auth1_token
//! - `SeatManagementService/WindsurfPostAuth` Б─■ exchange auth1_token for session_token
//!
//! Design points:
//! - Full same-origin requests (windsurf.com), no Google API Key involved, so no special Referer handling needed
//! - All REST requests include `Origin: https://windsurf.com` and `Referer: https://windsurf.com/account/login`
//!   to maintain consistency with browser behavior and avoid certain edge CSP rejections

use crate::utils::{AppError, AppResult};
use serde::{Deserialize, Serialize};
use std::sync::Arc;

/// Base URL for Devin password login (via windsurf.com same-origin proxy)
const DEVIN_AUTH_BASE_URL: &str = "https://windsurf.com/_devin-auth";
/// WindsurfPostAuth goes to main backend
const WINDSURF_BACKEND_URL: &str = "https://web-backend.windsurf.com";

// ==================== Request/Response Structures ===

#[derive(Debug, Serialize)]
pub struct ConnectionsRequest {
    pub product: String,
    pub email: String,
}

/// Response for `/_devin-auth/connections` (structure inferred from observation, unknown fields will be ignored)
#[derive(Debug, Deserialize, Serialize, Default)]
#[serde(default)]
pub struct ConnectionsResponse {
    /// List of available connection methods (raw JSON passthrough to tolerate field changes)
    #[serde(flatten)]
    pub raw: serde_json::Value,
}

#[derive(Debug, Serialize)]
pub struct PasswordLoginRequest {
    pub email: String,
    pub password: String,
}

/// Response for `/_devin-auth/password/login`
///
/// Actual response body example from testing:
/// ```json
/// {
///   "token": "auth1_yjk627jgorikbvs5u5r5ubsjszuuugu6uwvbecarhidxhmntl4zq",
///   "user_id": "user-5b148c75b9cb4bf2b48e1ae73ee13277",
///   "email": "aporter755@fnsdghm.shop"
/// }
/// ```
///
/// Field aliases also compatible with early speculated names (`auth1_token` / `account_id`).
#[derive(Debug, Deserialize, Serialize)]
pub struct PasswordLoginResponse {
    /// Primary authentication token (format: `auth1_<52 random characters>`)
    ///
    /// Actual API returns field name `token`; here we keep `auth1_token` as the Rust-side main name,
    /// and use alias to be compatible with `token` / `auth1Token` / `auth_token`.
    #[serde(alias = "token", alias = "auth1Token", alias = "auth_token")]
    pub auth1_token: String,
    /// Devin account/user ID (actual returned field is `user_id`, format: `user-<32 characters>`)
    #[serde(default, alias = "user_id", alias = "userId", alias = "accountId")]
    pub account_id: Option<String>,
    /// Returned email (server will echo back for verification)
    #[serde(default)]
    pub email: Option<String>,
    /// Preserve unknown fields for debugging
    #[serde(flatten)]
    pub extra: serde_json::Value,
}

/// Parsed response structure for `WindsurfPostAuth`
#[derive(Debug, Serialize, Deserialize, Clone, Default)]
pub struct WindsurfPostAuthResult {
    pub session_token: String,
    #[serde(default)]
    pub auth1_token: Option<String>,
    #[serde(default)]
    pub account_id: Option<String>,
    #[serde(default)]
    pub primary_org_id: Option<String>,
    #[serde(default)]
    pub orgs: Vec<WindsurfOrg>,
}

#[derive(Debug, Serialize, Deserialize, Clone, Default)]
pub struct WindsurfOrg {
    pub id: String,
    pub name: String,
}

/// Response parsing structure for `CheckUserLoginMethod`
///
/// Source: `exa.seat_management_pb.SeatManagementService/CheckUserLoginMethod`
///
/// Proto schema (field numbers restored from official website frontend chunk):
/// - 1: `redirect_url` (string)      Б─■ multi-region/security-wide redirect URL, empty means no redirect
/// - 2: `disallow_enterprise_user_login` (bool) Б─■ enterprise users prohibited from normal login
/// - 3: `user_exists` (bool)          Б─■ whether this email exists on WS(Firebase) side
/// - 4: `is_migrated` (bool)          Б─■ whether migrated to Auth1
/// - 5: `has_password` (bool)         Б─■ whether password credentials have been set
#[derive(Debug, Serialize, Deserialize, Clone, Default)]
pub struct CheckUserLoginMethodResult {
    #[serde(default)]
    pub redirect_url: String,
    #[serde(default)]
    pub disallow_enterprise_user_login: bool,
    #[serde(default)]
    pub user_exists: bool,
    #[serde(default)]
    pub is_migrated: bool,
    #[serde(default)]
    pub has_password: bool,
}

/// Login method sniffing aggregation result
///
/// Generated by [`DevinAuthService::sniff_login_method`], aggregating detection data from both sides:
/// - Firebase(WS) side: `CheckUserLoginMethod`
/// - Devin side:  `/_devin-auth/connections`
///
/// `recommended` field values and meanings (aligned with official website reducer BOTH_CHECKS_DONE):
/// - `"firebase"`    Б─■ old Firebase account + password set, use `signInWithEmailAndPassword`
/// - `"devin"`       Б─■ migrated or new Auth1 account, use Devin password login
/// - `"sso"`         Б─■ connected enterprise SSO, must complete SSO redirect in browser
/// - `"no_password"` Б─■ old account only used Google/GitHub, need OAuth or reset password first
/// - `"not_found"`   Б─■ email doesn't exist on either side, suggest registration first
/// - `"blocked"`     Б─■ enterprise user restricted from normal login channel
#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct LoginMethodSniffResult {
    /// Recommended login method
    pub recommended: String,
    /// Human-readable reason explanation (can be displayed directly to UI)
    pub reason: String,

    // ==== Firebase(WS) side original determination ===
    pub user_exists: bool,
    pub is_migrated: bool,
    pub has_password: bool,
    pub redirect_url: Option<String>,
    pub disallow_enterprise: bool,

    // ==== Devin side original determination ===
    /// Raw JSON returned by Devin `/connections`, `None` when interface fails/email doesn't exist
    pub devin_connections: Option<serde_json::Value>,
    /// Devin side `method` field: `"auth1"` | `"not_found"` | null
    pub devin_method: Option<String>,
    /// Devin side `has_password` field
    pub devin_has_password: Option<bool>,
    /// Whether Devin side `sso_connections` array is non-empty
    pub has_sso_connection: bool,
}

/// Final result of complete login flow
#[derive(Debug, Serialize, Clone)]
pub struct DevinLoginResult {
    pub session_token: String,
    pub auth1_token: String,
    pub account_id: Option<String>,
    pub primary_org_id: Option<String>,
    pub orgs: Vec<WindsurfOrg>,
    /// When multiple organizations are returned, user needs to make a second selection and call windsurf_post_auth(auth1_token, chosen_org_id)
    pub requires_org_selection: bool,
}

